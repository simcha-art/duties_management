"""
למה הפונקציות חולקו כך?
==========================

1. הפרדה בין UI ללוגיקה עסקית:
   - main.py: אחראי על קלט/פלט ותפריט
   - soldier_manager.py, duty_manager.py: לוגיקה עסקית טהורה
   
   יתרונות:
   - קל לבדוק את הלוגיקה בנפרד מהממשק
   - אפשר להחליף את הממשק (למשל, GUI) בלי לשנות לוגיקה
   - קוד נקי ומסודר יותר

2. הפרדה בין ניהול חיילים לניהול תורנויות:
   - soldier_manager.py: כל מה שקשור לחיילים
   - duty_manager.py: כל מה שקשור לתורנויות
   
   יתרונות:
   - כל קובץ עוסק בתחום אחד
   - קל למצוא איפה נמצאת פונקציה
   - קל להרחיב את המערכת

3. פונקציות עזר ב-utils.py:
   - פונקציות שחוזרות על עצמן
   - פונקציות שמשמשות מספר קבצים
   
   יתרונות:
   - DRY (Don't Repeat Yourself)
   - אם צריך לשנות לוגיקה, משנים במקום אחד
   - קוד קצר ויותר קריא

4. פונקציות handle_* ב-main.py:
   - כל פעולה מהתפריט = פונקציה אחת
   
   יתרונות:
   - main() לא הופכת לפונקציה ענקית
   - כל פעולה מטופלת בנפרד
   - קל להוסיף פעולות חדשות

5. שימוש ב-Exceptions בלוגיקה עסקית:
   - פונקציות לוגיקה עסקית זורקות exceptions במקרה של שגיאה
   - פונקציות validation מחזירות bool
   - main.py תופס את ה-exceptions ומציג הודעות מתאימות
   - הפרדה ברורה בין לוגיקה להצגת הודעות
   - קוד נקי יותר - אין צורך לבדוק True/False בכל מקום

למה לא יצרנו פונקציות קטנות מדי?
===================================

- לא יצרנו פונקציה נפרדת לכל בדיקה קטנה
- לא פיצלנו פונקציות שעושות דבר אחד פשוט
- שמרנו על איזון בין מודולריות לפשטות

"""